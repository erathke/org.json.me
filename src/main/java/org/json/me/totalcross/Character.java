package org.json.me.totalcross;



/**
 * The Char class wraps a value of the primitive type boolean in an object.
 * 
 * @author evandro
 */
public class Character
{

	private char value;

	public Character(char b)
	{
		this.value = b;
	}

	public char charValue()
	{
		return value;
	}
}
